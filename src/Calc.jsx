function add(x,y){
    const answer=x+y;
    return answer;
}

function sub(x,y){
    const answer=x-y;
    return answer;
}

function product(x,y){
    const answer=x*y;
    return answer;
}

function quotient(x,y){
    const answer=x/y;
    return answer.toFixed(2);
}





export {add,sub,product,quotient};